    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Para añadir roles a un usuario
        int[] intcuentaroles= listaRoles.getSelectedIndices();
        int[] intcuentausuarios= listaUsuarios.getSelectedIndices();
        if (intcuentaroles.length>0 && intcuentausuarios.length>0){
            int intidRol = rol.getIdRol();
            int intIdUsuario = usu.getIdUsuario();
            boolean retorno = objManejadorRolUsuario.RegistrarRolUsuario(intidRol, intIdUsuario);
            if (retorno==true){
                JOptionPane.showMessageDialog(this, "Rol Asignado con exito");
                ListarRolUsuario();
            }
            else{
                JOptionPane.showMessageDialog(this, "Error al registrar Rol/Usuario");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar al menos un Rol y un Usuario");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

