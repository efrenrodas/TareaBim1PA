    private void btnDesasignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesasignarActionPerformed
        //Recupera la seleccion de filas y Columnas
        int row = (int) jTableRolUsuario.getSelectedRow();
        //Obtiene el valor de la seleccion
        String IdRol = (String) jTableRolUsuario.getModel().getValueAt(row, 0);
        String IdUsu = (String) jTableRolUsuario.getModel().getValueAt(row, 1);
        boolean resultado = objManejadorRolUsuario.DesasignarRolUsuario(Integer.parseInt(IdRol), Integer.parseInt(IdUsu));
        if (resultado==true){
            JOptionPane.showMessageDialog(this, "Rol/Usuario Desasignado con exito");
            ListarRolUsuario();
        }
        else{
            JOptionPane.showMessageDialog(this, "Error al desasignar Rol/Usuario");
        }
    }//GEN-LAST:event_btnDesasignarActionPerformed
    public void CargarRoles() throws SQLException{
        DefaultListModel<Rol> dlm = new DefaultListModel<>();
        ArrayList<Rol> lstRol = objManejadorRoles.BuscarRol();
        Rol objRol = null;
        for (Rol lstRol1 : lstRol) {
            objRol = new Rol(lstRol1.getIdRol(), lstRol1.getRolNombre());
            dlm.addElement(objRol);
        }
        listaRoles.setModel(dlm);
    }
    
    public void CargarUsuarios() throws SQLException{
        DefaultListModel<Usuario> dlm = new DefaultListModel<>();
        ArrayList<Usuario> lstUsuario = objManejadorUsuario.Buscar("-1");
        Usuario objUsuario = null;
        for (Usuario lstUsuario1 : lstUsuario) {
            objUsuario = new Usuario(lstUsuario1.getIdUsuario(), lstUsuario1.getUsNombres());
            dlm.addElement(objUsuario);
        }
       // listaUsuarios.setModel(dlm);
       listaUsuarios.setModel(dlm);
    }
    
    public void ListarRolUsuario(){
    try {
        ArrayList<RolUsuario> lstRolUsuario =  objManejadorRolUsuario.BuscarRolUsuario();
        /*Es necesario crear un objeto con las columnas que se mostrarán en la tabla*/
        Object columnas[] = {"rolId", "usId", "rolNombre", "usNombres"};
        /*Es necesario añadir las columnas al modelo de la Tabla*/
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);
        /*Añadidas las columnas y el modelo se setea en la Tabla*/
        jTableRolUsuario.setModel(modelo);
        for (RolUsuario objRolUsuario : lstRolUsuario) {
            String idRol = String.valueOf(objRolUsuario.getIdRol());
            String idUs = String.valueOf(objRolUsuario.getIdUsuario());
            String rolNombre = objRolUsuario.getRolNombre();
            String usNombre = objRolUsuario.getUsNombres();
            String modeloTemp[] = {idRol, idUs, rolNombre, usNombre};
            modelo.addRow(modeloTemp);
        }
        this.pack();            
        } catch (SQLException ex) {
            Logger.getLogger(IFRolUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
