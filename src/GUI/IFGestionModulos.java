  public void Consultar() throws SQLException{
       ArrayList<Modulo> lstMod = objManejadorMod.BuscarMod();
       /*Es necesario crear un objeto con las columnas que se mostrar谩n en la tabla*/
       Object columnas[] = {"idModulo", "modNombre", "modEstado"};
       /*Es necesario a帽adir las columnas al modelo de la Tabla*/
       DefaultTableModel modelo = new DefaultTableModel(null, columnas);
       /*A帽adidas las columnas y el modelo se setea en la Tabla*/
       jTable1.setModel(modelo);
       for (Modulo objMod : lstMod) {
           String idModulo = String.valueOf(objMod.getIdModulo());
           String modNombre = objMod.getModNombre();
           String modEstado = objMod.getModEstado();
           String modeloTemp[] = {idModulo, modNombre, modEstado};
           modelo.addRow(modeloTemp);
       }
       this.pack();
   }
  private boolean Eliminar(int id) {
        boolean estado = objManejadorMod.Eliminar(id);
    return estado;
   }
