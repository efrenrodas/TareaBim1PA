    public void CargarCatalogo() throws SQLException{
        ArrayList<ItemCatalogo> lstEstado = new ArrayList<ItemCatalogo>();
        ArrayList<ItemCatalogo> lstPais = new ArrayList<ItemCatalogo>();
        ArrayList<ItemCatalogo> lstGenero = new ArrayList<ItemCatalogo>();
        lstEstado =  objManejadorCatalogo.BuscarItemporCatNombre("ESTADO");
        for (int i=0; i<lstEstado.size();i++){
            int idItem = lstEstado.get(i).getIdItemCatalogo();
            String strNombre = lstEstado.get(i).getItcNombre();
            cmbEstado.insertItemAt(strNombre, i);
        }
        
        lstPais =  objManejadorCatalogo.BuscarItemporCatNombre("PAIS");
        for (int i=0; i<lstPais.size();i++){
            int idItem = lstPais.get(i).getIdItemCatalogo();
            String strNombre = lstPais.get(i).getItcNombre();
            cmbPais.insertItemAt(strNombre, i);
        }
        
        lstGenero =  objManejadorCatalogo.BuscarItemporCatNombre("GENERO");
        for (int i=0; i<lstGenero.size();i++){
            int idItem = lstGenero.get(i).getIdItemCatalogo();
            String strNombre = lstGenero.get(i).getItcNombre();
            cmbGenero.insertItemAt(strNombre, i);
        }        
        
    }
    
