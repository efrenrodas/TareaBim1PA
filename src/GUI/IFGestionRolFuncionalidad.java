    public void CargarRoles() throws SQLException{
        DefaultListModel<Rol> dlm = new DefaultListModel<>();
        ArrayList<Rol> lstRol = objManejadorRoles.BuscarRol();
        Rol objRol = null;
        for (Rol lstRol1 : lstRol) {
            objRol = new Rol(lstRol1.getIdRol(), lstRol1.getRolNombre());
            dlm.addElement(objRol);
        }
        listaRol.setModel(dlm);
    }
    
    public void CargarFuncionalidades() throws SQLException{
        DefaultListModel<Funcionalidad> dlm = new DefaultListModel<>();
        ArrayList<Funcionalidad> lstFuncionalidad = objManejadorFuncionalidad.Buscar("-1");
        Funcionalidad objFuncionalidad = null;
        for (Funcionalidad lstFuncionalidad1 : lstFuncionalidad) {
            objFuncionalidad = new Funcionalidad(lstFuncionalidad1.getIdFuncionalidad(), lstFuncionalidad1.getFunNombre());
            dlm.addElement(objFuncionalidad);
        }
        listaFuncionalidad.setModel(dlm);
    }
    
    public void ListarRolFuncionalidad(){
    try {
        ArrayList<RolFuncionalidad> lstRolFuncionalidad =  objManejadorRolFuncionalidad.BuscarRolFuncionalidad();
        /*Es necesario crear un objeto con las columnas que se mostrarán en la tabla*/
        Object columnas[] = {"rolId", "funId", "rolNombre", "funNombre"};
        /*Es necesario añadir las columnas al modelo de la Tabla*/
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);
        /*Añadidas las columnas y el modelo se setea en la Tabla*/
        jTableRolFunc.setModel(modelo);
        for (RolFuncionalidad objRolFunc : lstRolFuncionalidad) {
            String idRol = String.valueOf(objRolFunc.getIdRol());
            String idFun = String.valueOf(objRolFunc.getIdFuncionalidad());
            String rolNombre = objRolFunc.getRolNombre();
            String funNombre = objRolFunc.getFunNombre();
            String modeloTemp[] = {idRol, idFun, rolNombre, funNombre};
            modelo.addRow(modeloTemp);
        }
        this.pack();            
        } catch (SQLException ex) {
            Logger.getLogger(IFRolUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

