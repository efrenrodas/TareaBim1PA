    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        /* Para añadir funcionalidades a un rol
        Aqui hay que hacer un cambio para que varias funcionalidades puedan añadirse a un Rol*/
        int[] intcuentaroles= listaRol.getSelectedIndices();
        int[] intcuentafuncionalidad= listaFuncionalidad.getSelectedIndices();
        if (intcuentaroles.length>0 && intcuentafuncionalidad.length>0){
            int intidRol = rol.getIdRol();
            int intIdFunc = func.getIdFuncionalidad();
            boolean retorno = objManejadorRolFuncionalidad.RegistrarRolFuncionalidad(intidRol, intIdFunc);
            if (retorno==true){
                JOptionPane.showMessageDialog(this, "Funcionalidades asignadas con exito");
                ListarRolFuncionalidad();
            }
            else{
                JOptionPane.showMessageDialog(this, "Error al registrar Rol/Funcionalidades");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar al menos un Rol y una o varias Funcionalidades");
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

