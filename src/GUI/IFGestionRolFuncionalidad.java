    private void btnDesasignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesasignarActionPerformed
        // nada por aca
          //Recupera la seleccion de filas y Columnas
        int row = (int) jTableRolFunc.getSelectedRow();
        //Obtiene el valor de la seleccion
        String IdRol = (String) jTableRolFunc.getModel().getValueAt(row, 0);
        String IdFun = (String) jTableRolFunc.getModel().getValueAt(row, 1);
        boolean resultado = objManejadorRolFuncionalidad.DesasignarRolFuncionalidad(Integer.parseInt(IdRol), Integer.parseInt(IdFun));
        if (resultado==true){
            JOptionPane.showMessageDialog(this, "Rol/Funcionalidad Desasignado con exito");
            ListarRolFuncionalidad();
        }
        else{
            JOptionPane.showMessageDialog(this, "Error al desasignar Rol/Funcionalidad");
        }
    }//GEN-LAST:event_btnDesasignarActionPerformed


