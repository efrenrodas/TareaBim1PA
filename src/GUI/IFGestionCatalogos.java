    public boolean GuardarItemCatalogo() throws SQLException{

        int row = (int) jTableCatalogo.getSelectedRow();
        int column = (int) jTableCatalogo.getSelectedColumn();
        //Obtiene el valor del Catalogo para asociarlo al Item
        String IdCatalogo = (String) jTableCatalogo.getModel().getValueAt(row, 0);
        ItemCatalogo objItemCatalogo = new ItemCatalogo(-1, txtItcCod.getText(), txtItcNombre.getText(), txtItcDescripcion.getText(), Integer.parseInt(IdCatalogo));
        LimpiarCajasItem();
        ConsultarItemCatalogo(Integer.parseInt(IdCatalogo));
        return objManejadorCatalogos.RegistrarItemCatalogo(Integer.parseInt(IdCatalogo), objItemCatalogo);
        
    }
    
