    private void btnGuardarCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCatActionPerformed
        try{
            if (accion.equals("guardar")) {
              if (txtcatCodigo.getText()!=null && txtcatNombre.getText()!=null && txtcatDescripcion.getText()!=null){
                String strCatCodigo = txtcatCodigo.getText();
                String strCatNombe = txtcatNombre.getText();
                String strCatDescripcion = txtcatDescripcion.getText();
                int intCatEstado = cmbEstado.getSelectedIndex(); 
                Catalogo objCatalogo = new Catalogo(-1, strCatCodigo, strCatNombe, strCatDescripcion, intCatEstado);
                boolean resultado = GuardarCatalogo(objCatalogo);
                if (resultado==true){
                    JOptionPane.showMessageDialog(this, "Datos almacenados con exito");
                    RecuperaDatos();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Error al guardar los datos");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Debe ingresar todos los datos de Catálogo");
            }  
            }
            else{
            //codigo de actualizar 
              int id= Integer.parseInt(Id);
            if (txtcatCodigo.getText()!=null && txtcatNombre.getText()!=null && txtcatDescripcion.getText()!=null){
                String strCatCodigo = txtcatCodigo.getText();
                String strCatNombe = txtcatNombre.getText();
                String strCatDescripcion = txtcatDescripcion.getText();
                int intCatEstado = cmbEstado.getSelectedIndex(); 
                Catalogo objCatalogo = new Catalogo(id, strCatCodigo, strCatNombe, strCatDescripcion, intCatEstado);
                boolean resultado = ActualizarCatalogo(objCatalogo);
                if (resultado==true){
                    JOptionPane.showMessageDialog(this, "Datos almacenados con exito");
                    RecuperaDatos();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Error al guardar los datos");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Debe ingresar todos los datos de Catálogo");
            }  
            accion="guardar";
            }
            
        }
        catch(Exception ex){
            String msn = ex.getMessage();
        }
    }//GEN-LAST:event_btnGuardarCatActionPerformed

