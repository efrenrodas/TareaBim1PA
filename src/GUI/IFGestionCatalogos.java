    public void ConsultarItemCatalogo(int intId) throws SQLException{
    int intExistencia = objManejadorCatalogos.ContarItemCatalogo(intId);
    
        if (intExistencia>0)
        {
            ArrayList<ItemCatalogo> lstItemCatalogo = objManejadorCatalogos.BuscarItemCatalogo(intId);
            /*Es necesario crear un objeto con las columnas que se mostrarán en la tabla*/
                Object columnas[] = {"idItemCatalogo", "itcCodigo", "itcNombre", "itcDescripcion", "idCatalogo"};
                /*Es necesario añadir las columnas al modelo de la Tabla*/
                DefaultTableModel modelo = new DefaultTableModel(null, columnas);
                /*Añadidas las columnas y el modelo se setea en la Tabla*/
                jTableItemCatalogo.setModel(modelo);
                for (ItemCatalogo objICat : lstItemCatalogo) {
                    String id = String.valueOf(objICat.getIdItemCatalogo());
                    String codigo = objICat.getItcCodigo();
                    String nombre = objICat.getItcNombre();
                    String descripcion = objICat.getItcDescripcion();
                    String catId = String.valueOf(objICat.getIdCatalogo());
                    //Por cada registro se lo añade a una estructra tipo Arreglo para incluirlo en la Tabla
                    String modeloTemp[] = {id, codigo, nombre, descripcion, catId};
                    //Se añade la estructura al modelo de la Tabla
                    modelo.addRow(modeloTemp);
                }
                this.pack();
        }
        else{
            Object columnas[] = {"idItemCatalogo", "itcCodigo", "itcNombre", "itcDescripcion", "idCatalogo"};
            DefaultTableModel modelo = new DefaultTableModel(null, columnas);
            jTableItemCatalogo.setModel(modelo);
            this.pack();
            txtItcCod.setText(null);
            txtItcNombre.setText(null);
            txtItcDescripcion.setText(null);
        }
    }
    
    
    
    
    public void RecuperaDatos() throws ClassNotFoundException, IOException, SQLException{
        ArrayList<Catalogo> lstCatalogo = objManejadorCatalogos.Buscar("-1");
        /*Es necesario crear un objeto con las columnas que se mostrarán en la tabla*/
        Object columnas[] = {"idcatalogo", "catCodigo", "catNombre", "catDescripcion", "catEstado"};
        /*Es necesario añadir las columnas al modelo de la Tabla*/
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);
        /*Añadidas las columnas y el modelo se setea en la Tabla*/
        jTableCatalogo.setModel(modelo);
        for (Catalogo objCat : lstCatalogo) {
            String id = String.valueOf(objCat.getIdCatalogo());
            String codigo = objCat.getCatCodigo();
            String nombre = objCat.getCatNombre();
            String descripcion = objCat.getCatDescripcion();
            String estado = String.valueOf(objCat.getCatEstado());
            //Por cada registro se lo añade a una estructra tipo Arreglo para incluirlo en la Tabla
            String modeloTemp[] = {id, codigo, nombre, descripcion, estado};
            //Se añade la estructura al modelo de la Tabla
            modelo.addRow(modeloTemp);
        }
        this.pack();
    }
    
