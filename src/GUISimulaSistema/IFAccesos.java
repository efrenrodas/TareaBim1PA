public boolean Login(String UsUsuuario, String Clave) throws SQLException{
boolean correcto = false;

ArrayList<Usuario>  lstUsuario = objManejadorUsuarios.Login(UsUsuuario);
 for (Usuario usu : lstUsuario) {
     String clave=usu.getUsPassword();
   
     if (clave.equals(Clave)) {
         System.out.println("Logeo correcto");
    correcto=true;
     }
     else
     {System.out.println("Usuario no existe");
     correcto=false;}
 }

return correcto;
}

